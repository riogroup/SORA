def get_archinal_frame(spkid):
    """

    Parameters
    ----------
    spkid : `str`
        Spkid of the object.

    Returns
    -------
    frame : `sora.body.frame.PlanetocentricFrame`
        A PlanetrocentricFrame with the parameters from Archinal et al. (2018)

    """
    parameters = archinal_2018.get(str(spkid))
    if not parameters:
        raise ValueError(f"Orientation for object {spkid} is not present in Archinal et al. (2018)")
    pole, ap, dp, prime, rot_vel, rh = parameters
    pole = SkyCoord(pole, unit=('deg', 'deg'), frame='icrs')
    epoch = Time('J2000', scale='tdb')
    frame = PlanetocentricFrame(epoch=epoch, pole=pole, alphap=ap, deltap=dp, prime_angle=prime, rotation_velocity=rot_vel,
                                right_hand=rh, reference=reference, **precession.get(str(spkid), {}))
    return frame
